# bin_spatial
# Takes an RGB image and converts it into another color space and size,
# and unrolls it into one long vector. Courtesy of Udacity.
# ---------------------------------------------------------
def bin_spatial(img, size=(32, 32)):
    # Use cv2.resize().ravel() to create the feature vector
    features = cv2.resize(img, size).ravel() 
    # Return the feature vector
    return features

# color_hist
# Take an RGB image and compute the histograms of the three color channels,
# concatenating them into one vector. Courtesy of Udacity.
# --------------------------------------------------------
# the lesson has bins_range=(0, 256), don't need and image is between 0 and 1.
def color_hist(img, nbins=32, bins_range=(0, 256)):
    # Compute the histogram of the RGB channels separately
    rhist = np.histogram(img[:,:,0], bins=nbins, range=bins_range)
    ghist = np.histogram(img[:,:,1], bins=nbins, range=bins_range)
    bhist = np.histogram(img[:,:,2], bins=nbins, range=bins_range)
    # Generating bin centers
    bin_edges = rhist[1]
    bin_centers = (bin_edges[1:]  + bin_edges[0:len(bin_edges)-1])/2
    # Concatenate the histograms into a single feature vector
    hist_features = np.concatenate((rhist[0], ghist[0], bhist[0]))
    # Return the individual histograms, bin_centers and feature vector
    return hist_features

